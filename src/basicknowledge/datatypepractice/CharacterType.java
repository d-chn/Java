package basicknowledge.datatypepractice;

public class CharacterType {
    public static void main(String[] args) {
        char c1 = 'A';
        char c2 = '\u0041';
        char c3 = 'жа';
        System.out.println("c1 = " + c1);
        System.out.println("c2 = " + c2);
        System.out.println("c3 = " + c3);
    }
}
