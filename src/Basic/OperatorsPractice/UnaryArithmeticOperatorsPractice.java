package Basic.OperatorsPractice;

public class UnaryArithmeticOperatorsPractice {
    public static void main(String[] args) {
        int a = 10;
        System.out.printf("-a is %d%n",-a);
        System.out.printf("++a is %d%n",++a);
        System.out.printf("a++ is %d%n",a++);
        System.out.printf("a is %d%n",a);
    }
}
